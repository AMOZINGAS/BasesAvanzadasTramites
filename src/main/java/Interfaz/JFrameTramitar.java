    private void btnPlacaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPlacaMouseClicked
        // TODO add your handling code here:
        
        JOptionPane panel = new JOptionPane("¿Que tipo de tramite es?", JOptionPane.INFORMATION_MESSAGE, JOptionPane.OK_CANCEL_OPTION, null, new String[]{"Nuevo", "Renovación"});
        
        JDialog dialogo = panel.createDialog("Información");
        
        dialogo.setVisible(true);
            
        if(panel.getValue().equals("Nuevo")){
            
            //NUEVO
            JFrameAgregarVehiculo vehiculo = new JFrameAgregarVehiculo();
            vehiculo.setVisible(true);
            this.dispose();
            
        }else if(panel.getValue().equals("Renovación")){
            
            //RENOVACION
            JFrameTramitePlacas placas = new JFrameTramitePlacas(TipoPlaca.RENOVADO);
            placas.setVisible(true);
            this.dispose();
            
            
        }else{
            
            this.dispose();
            
        }
        
    }//GEN-LAST:event_btnPlacaMouseClicked

